export function FormatSpell(spellId){
  switch(spellId){
    case(4):
      return "_flash"    
    case(54):
      return "_empty"    
    case(7):
      return "_heal"    
    case(11):
      return "_smite"    
    case(12):
      return "_teleport"    
    case(32):
      return "_mark"    
    case(3):
      return "_exhaust"    
    case(6):
      return "_haste"    
    case(14):
      return "ignite"    
    case(21):
      return "barrier"    
  }
}